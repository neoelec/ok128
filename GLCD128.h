/* -------------------------------------------------------------------------- */
/*   Graphic LCD Module : Hyundai LCD,HG12605NY-LY,128x64 dot,LED backlight   */
/* -------------------------------------------------------------------------- */
/* LCD_DATABUS(0x2000) : D0-D7 = DB0-DB7 (7-14, data bus)		      */
/* LCD_CONTROL(0x2100) :   GND = R/-W    (5, read/write)		      */
/*                          D4 = D/-I    (4, data/instruction)		      */
/*                          D5 = E       (6, enable)			      */
/*                          D6 = CS1     (15, chip select 1)		      */
/*                          D7 = CS2     (16, chip select 2)		      */

uint8_t xcharacter, ycharacter;                // x character(0-3), y character(0-15)
uint8_t cursor_flag, xcursor, ycursor;         // x and y cursor position(0-3, 0-15)

void GLCD_command(uint8_t signal, uint8_t command)
{                                              /* write a command */
  LCD_CONTROL = signal & 0xC0;                 // D/-I = 0
  asm volatile (" PUSH  R0 ");                 // delay for 500 ns
  asm volatile (" POP   R0 ");
  asm volatile (" PUSH  R0 ");
  asm volatile (" POP   R0 ");
  LCD_DATABUS = command;                       // write command
  LCD_CONTROL = (signal & 0xC0) | 0x20;        // E = 1
  asm volatile (" PUSH  R0 ");                 // delay for 500 ns
  asm volatile (" POP   R0 ");
  asm volatile (" PUSH  R0 ");
  asm volatile (" POP   R0 ");
  LCD_CONTROL = signal & 0xC0;                 // E = 0
  asm volatile (" NOP      ");
  LCD_CONTROL = 0x00;                          // clear all control signals
  Delay_us(10);                                // wait for GLCD busy
}

void GLCD_data(uint8_t signal, uint8_t character)
{                                              /* write a data */
  LCD_CONTROL = (signal & 0xC0) | 0x10;        // D/-I = 1
  asm volatile (" PUSH  R0 ");                 // delay for 500 ns
  asm volatile (" POP   R0 ");
  asm volatile (" PUSH  R0 ");
  asm volatile (" POP   R0 ");
  LCD_DATABUS = character;                     // write data
  LCD_CONTROL = (signal & 0xC0) | 0x30;        // E = 1
  asm volatile (" PUSH  R0 ");                 // delay for 500 ns
  asm volatile (" POP   R0 ");
  asm volatile (" PUSH  R0 ");
  asm volatile (" POP   R0 ");
  LCD_CONTROL = (signal & 0xC0) | 0x10;        // E = 0
  asm volatile (" NOP      ");
  LCD_CONTROL = 0x00;                          // clear all control signals
  Delay_us(10);                                // wait GLCD busy
}

void GLCD_clear(void)
{                                              /* clear GLCD screen */
  uint8_t i, j, x;

  LCD_CONTROL = 0x00;                          // clear all control signals
  GLCD_command(0xC0, 0x3F);                    // CS1,CS2 display ON
  GLCD_command(0xC0, 0xC0);                    // CS1,CS2 display position

  x = 0xB8;
  for (i = 0; i <= 7; i++) {
    GLCD_command(0xC0, x);                     // X start addtess
    GLCD_command(0xC0, 0x40);                  // Y start address
    for (j = 0; j <= 63; j++)
      GLCD_data(0xC0, 0x00);                   // clear CS1 and CS2 screen
    x++;
  }
}

void GLCD_xy(uint8_t x, uint8_t y)
{                                              /* set English character position */
  xcharacter = x;                              // x = 0 - 3
  ycharacter = y;                              // y = 0 - 15
}

void GLCD_xy_row(uint8_t x, uint8_t y, uint8_t row)
{                                              /* set character upper/lower row */
  if (row == 0)                                // if upper row, set Y address
    GLCD_command(0xC0, 0xB8 + x * 2);
  else                                         // if lower row, set Y address + 1
    GLCD_command(0xC0, 0xB8 + x * 2 + 1);

  if (y <= 7)                                  // if y <= 7, CS1 Y address
    GLCD_command(0x40, 0x40 + y * 8);
  else                                         // if y >= 8, CS2 Y address
    GLCD_command(0x80, 0x40 + (y - 8) * 8);
}

void GLCD_English(uint8_t Ecode)
{                                              /* display a 8x16 Dot English(ASCII) */
  unsigned char i, signal;

  GLCD_xy_row(xcharacter, ycharacter, 0);      // display upper row
  if (ycharacter <= 7)                         // if y <= 7, CS1
    signal = 0x40;
  else                                         // if y >= 8, CS2
    signal = 0x80;
  for (i = 0; i <= 7; i++)
    GLCD_data(signal, pgm_read_byte(&E_font[Ecode][i]));

  GLCD_xy_row(xcharacter, ycharacter, 1);      // display lower row
  if (ycharacter <= 7)                         // if y <= 7, CS1
    signal = 0x40;
  else                                         // if y >= 8, CS2
    signal = 0x80;
  for (i = 8; i <= 15; i++) {
    if ((cursor_flag == 1) && (xcharacter == xcursor) && (ycharacter == ycursor))
      GLCD_data(signal, pgm_read_byte(&E_font[Ecode][i]) | 0x80);
    else
      GLCD_data(signal, pgm_read_byte(&E_font[Ecode][i]));
  }

  ycharacter++;                                // go next character position
  if (ycharacter == 16) {
    ycharacter = 0;
    xcharacter++;
  }
}

void GLCD_Korean(uint16_t Kcode)
{                                              /* display a 16x16 Dot Korean */
  unsigned char i, signal;
  unsigned char cho_5bit, joong_5bit, jong_5bit;
  unsigned char cho_bul, joong_bul, jong_bul = 0, jong_flag;
  unsigned int character;
  unsigned char Korean_buffer[32];             // 32 byte Korean font buffer

  if (ycharacter == 15)                        // if can not display, go next line
    GLCD_English(0x20);

  // get cho, joong, jong 5 bit
  cho_5bit = pgm_read_byte(&table_cho[(Kcode >> 10) & 0x001F]);
  joong_5bit = pgm_read_byte(&table_joong[(Kcode >> 5) & 0x001F]);
  jong_5bit = pgm_read_byte(&table_jong[Kcode & 0x001F]);

  if (jong_5bit == 0) {                        // don't have jongsung
    jong_flag = 0;
    cho_bul = pgm_read_byte(&bul_cho1[joong_5bit]);
    if ((cho_5bit == 1) || (cho_5bit == 16))
      joong_bul = 0;
    else
      joong_bul = 1;
  } else {                                     // have jongsung
    jong_flag = 1;
    cho_bul = pgm_read_byte(&bul_cho2[joong_5bit]);
    if ((cho_5bit == 1) || (cho_5bit == 16))
      joong_bul = 2;
    else
      joong_bul = 3;
    jong_bul = pgm_read_byte(&bul_jong[joong_5bit]);
  }

  character = cho_bul * 20 + cho_5bit;         // copy chosung font
  for (i = 0; i <= 31; i++)
    Korean_buffer[i] = pgm_read_byte(&K_font[character][i]);

  character = 8 * 20 + joong_bul * 22 + joong_5bit; // OR joongsung font
  for (i = 0; i <= 31; i++)
    Korean_buffer[i] |= pgm_read_byte(&K_font[character][i]);

  if (jong_flag == 1) {                        // if jongsung, OR jongsung font
    character = 8 * 20 + 4 * 22 + jong_bul * 28 + jong_5bit;
    for (i = 0; i <= 31; i++)
      Korean_buffer[i] |= pgm_read_byte(&K_font[character][i]);
  }

  GLCD_xy_row(xcharacter, ycharacter, 0);      // display upper left row
  if (ycharacter <= 7)                         // if y <= 7, CS1
    signal = 0x40;
  else                                         // if y >= 8, CS2
    signal = 0x80;
  for (i = 0; i <= 7; i++)
    GLCD_data(signal, Korean_buffer[i]);

  GLCD_xy_row(xcharacter, ycharacter, 1);      // display lower left row
  if (ycharacter <= 7)                         // if y <= 7, CS1
    signal = 0x40;
  else                                         // if y >= 8, CS2
    signal = 0x80;
  for (i = 16; i <= 23; i++) {
    if ((cursor_flag == 1) && (xcharacter == xcursor) && (ycharacter == ycursor))
      GLCD_data(signal, Korean_buffer[i] | 0x80);
    else
      GLCD_data(signal, Korean_buffer[i]);
  }

  ycharacter++;

  GLCD_xy_row(xcharacter, ycharacter, 0);      // display upper right row
  if (ycharacter <= 7)                         // if y <= 7, CS1
    signal = 0x40;
  else                                         // if y >= 8, CS2
    signal = 0x80;
  for (i = 8; i <= 15; i++)
    GLCD_data(signal, Korean_buffer[i]);

  GLCD_xy_row(xcharacter, ycharacter, 1);      // display lower right row
  if (ycharacter <= 7)                         // if y <= 7, CS1
    signal = 0x40;
  else                                         // if y >= 8, CS2
    signal = 0x80;
  for (i = 24; i <= 31; i++) {
    if ((cursor_flag == 1) && (xcharacter == xcursor) && (ycharacter == (ycursor + 1)))
      GLCD_data(signal, Korean_buffer[i] | 0x80);
    else
      GLCD_data(signal, Korean_buffer[i]);
  }

  ycharacter++;                                // go next character position
  if (ycharacter == 16) {
    ycharacter = 0;
    xcharacter++;
  }
}

void GLCD_string(uint8_t x, uint8_t y, uint8_t * string)
{                                              /* display a string */
  unsigned char character1;
  unsigned int character2;

  GLCD_xy(x, y);                               // x = 0 - 3, y = 0 - 15

  while (*string != '\0') {
    character1 = *string;
    string++;
    if (character1 < 0x80)
      GLCD_English(character1);                // display English character
    else {
      character2 = 256 * character1 + (*string & 0xFF);
      string++;
      GLCD_Korean(character2);                 // display Korean character
    }
  }
}
